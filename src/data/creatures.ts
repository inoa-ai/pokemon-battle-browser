type DamageEffect = Extract<Move['effect'], { kind: 'damage' }>;

const artUrl = (id: number) => `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/official-artwork/${id}.png`;

const damageMove = (
  id: string,
  name: string,
  type: ElementType,
  category: 'physical' | 'special',
  power: number,
  accuracy: number,
  maxPp: number,
  description: string,
  fx: string,
  extra: Partial<Omit<DamageEffect, 'kind'>> = {},
  priority = 0,
): Move => ({
  id,
  name,
  type,
  category,
  power,
  accuracy,
  maxPp,
  priority,
  effect: { kind: 'damage', ...extra },
  description,
  fx,
});

const statusMove = (
  id: string,
  name: string,
  type: ElementType,
  maxPp: number,
  description: string,
  fx: string,
  effect: Move['effect'],
  accuracy = 100,
  priority = 0,
): Move => ({
  id,
  name,
  type,
  category: 'status',
  power: 0,
  accuracy,
  maxPp,
  priority,
  effect,
  fx,
  description,
});

